a = 10
b = 3

print("Addition: ", a + b)
print("Subtraction: ", a - b)
print("Multiplication: ", a * b)
print("Division: ", a / b)        #gives a float as result
print("Floor Division: ", a // b) #gives an integer as result
print("Modulus: ", a % b)         #gives the remainder
print("Exponent: ", a ** b)